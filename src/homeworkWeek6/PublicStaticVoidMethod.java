package homeworkWeek6;

public class PublicStaticVoidMethod {
    // static method we don't have to creat object you can call method
    public static void run (){
        System.out.println("my program is running ");
    }
    public static void go (){
        System.out.println(" i am going to work");
    }

    public static void main(String[] args) {
        run();
        run();
        go();

    }
}
